      <tr>
        <th>Date</th>
        <td>2018</td>
      </tr>
      <tr>
        <th>Medium</th>
        <td>Sculpture: Interactive ceramic chimes</td>
      </tr>
      <tr>
        <th>Dimensions</th>
        <td>8m x 6m x 4.5m</td>
      </tr>
      <tr>
        <th>Description</th>
        <td>A collaboration with artist Hui-Ying Tsai, this piece is an interactive ceramic chime that plays the sound of the nature/culture of the Pacific islands. The soundscape triggered by the viewers touching the pieces transcends the space and time, and connect the viewers to the land, the ocean, and the past. This piece honors the Austronesian culture and expresses human’s respect to nature since the prehistoric era across all cultures.</td>
